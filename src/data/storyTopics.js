/**
 * Get a random story topic
 * @returns {string} A random topic from the list
 */
export const getRandomTopic = () => {
  const randomIndex = Math.floor(Math.random() * storyTopics.length);
  return storyTopics[randomIndex];
};

/**
 * Get a specified number of random unique topics
 * @param {number} count - Number of topics to return
 * @returns {string[]} Array of unique random topics
 */
export const getRandomTopics = count => {
  const shuffled = [...storyTopics].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, storyTopics.length));
};
